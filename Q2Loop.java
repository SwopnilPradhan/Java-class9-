import java.util.*;
public class Q2Loop
{
    public static void main(String args[]){
        Scanner sc = new Scanner(System.in);
        
        System.out.println("Enter number range :- ");
        int a = sc.nextInt();
        
        for(int i = 1; i <= a; i++){
            System.out.println(i);
        }
    }
}
